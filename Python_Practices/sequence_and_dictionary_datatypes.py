#sequence Datatypes
#1. List
language = ["swift", "Java", "python", "perl", 22, 10]
print(language)   #prints All lists
print(language[2])   #prints: python

#2. Tuple
language = ("swift", "Java", "python", "perl")
print(language)   #prints All lists
print(language[2])   #prints: python

#Dictionary datatypes
capital_city = {'Ethiopia':'AA', 'Italy':'Rome', 'UK':'London'}
print(capital_city)        #prints: All
print(capital_city["Ethiopia"])   #prints: AA
